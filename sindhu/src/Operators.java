public class Operators {
    public static void main(String[] args){
        int x=9;
        System.out.println(x++ + x++ + ++x + x++ + ++x + x++ + ++x);
        System.out.println(-x);
        int a=10;
        int b=-5;
        System.out.println(10<<3);
        System.out.println(10>>3);
        System.out.println(a>b && a>b);
        System.out.println(a<b | a>b);
        System.out.println(~b);



    }
    
}
