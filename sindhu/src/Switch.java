public class Switch {
    public static void main(String[] args){
        switch("friday"){
            case "monday":
            System.out.println("first");
            break;
            case "tuesday":
            System.out.println("second");
            break;
            case "wednesday":
            System.out.println("third");
            break;
            case "thursday":
            System.out.println("fourth");
            break;
            case "friday":
            System.out.println("fifth");
            break;
            default:
            System.out.println("sixth");

        }


    }
    
}
