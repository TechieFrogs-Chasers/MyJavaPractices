package Assignment;
public class InvHalfPyramidNumber {
    public static void main(String[] args) {
        int r = 5;//
        for(int i= r;i >=1;--i){
            //System.out.println(i);
           for(int j= 1;j <=i;++j) {
            System.out.print(j + " ");
            }
            System.out.println();
       }
    }
}
