public class HalfPyramid{
    public static void main(String[] args) {
        for(int i=1;i<=5;i++){
            for(int j=1;j<=i;j++)
            {
            System.out.print("* ");//print is used to print the output in same line
            }
            System.out.println();//new line
        }

    }
}


    

