public class Increment {
    public static void main(String[] args) {
        int a =10;
       System.out.println(a-- + a--);
        int b=10;
        System.out.println(--b + --b);
        int c=10;
        System.out.println(c++ + c++);
         int d=10;
        System.out.println(++d + ++d);
        int e=10;
        System.out.println(e++ + ++e);
        int a1=10;
        System.out.println(a1++ + --a1);
        
    }
    
}
