public class EvenPosition {
    public static void main(String[] args) {
        int[] num ={1,2,3,4,5,6,7,8,9};
        int i=0;
        for ( i=i+1; i<=num.length; i++){
            i=i+1;
            System.out.println("The elements that are present in the even number positions are "+num[i]);
        }
    }
    
}
