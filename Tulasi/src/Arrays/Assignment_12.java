package Arrays;

public class Assignment_12 {
    public static void main(String[] args) {
        int[] arr={9,51, 1};
        int sum=0;
        System.out.println(" Sum of numbers :");
        for (int i=0;i<arr.length;i++){
            sum=sum+arr[i];
            System.out.println(sum);
        }
    }
}
