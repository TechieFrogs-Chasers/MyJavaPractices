package Basicassignments;

public class Sandglassstarpattern {
    public static void main(String[] args) {
        int i;
        int stars=10;
        int rows=0;
        for( i=0;i<=rows;i++);
        for (int j = 1; j <i; j++) {
            System.out.print(" ");
        }
        for (int k = 1; k <= stars; k++) {
            System.out.print("*");
        }
        stars = stars - 1;
        System.out.println();

    }
}
